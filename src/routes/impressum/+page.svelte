    <h1>Datengrundlage</h1>
<a> Seit 2012 erfassen das Bundesamt für Umwelt </a> 
<a href="www.bafu.admin.ch">BAFU</a>
<a> zusammen mit dem Bundesamt für Landestopografie </a> 
<a href="www.swisstopo.admin.ch"> Swisstopo,</a>
<a> im Rahmen des Gletschermonitorings der Schweiz </a> 
<a href= "www.glamos.ch">GLAMOS,</a>
<a> jährlich fast 100 Gletscher und deren Vorfelder. Dabei entstehen hochaufgelöste Daten:</a>

<ul>
    <li>Luftbilder/Orthofotos mit 10–25 cm Auflösung</li>
    <li>Digitale Höhenmodelle mit 0,5–1 m Auflösung</li>
</ul> 

<a>Weitere Informationen zu den Daten finden gibt es <a href="https://www.swisstopo.admin.ch/de/aufnahme-luftbilder">hier</a>.</a>

<h1>Forschungsprojekt TapRep </h1>

<a>Das Projekt <b>TapRep</b> (Tapping the potential of one decade of annual repeat altimetry to study glacial and periglacial processes) nutzt diesen Datenbestand, um glaziologische und geomorphologische Fragestellungen zu untersuchen:</a>
<ul>
    <li>Extrapolation von lokalen Massenbilanzmessungen auf regionale Skalen (ETH Zürich)</li>
    <li>Analyse von Einsturztrichter auf Gletscherzungen (ETH Zürich)</li>
    <li>Sedimentdynamik in Gletschervorfeldern (Eawag, Universität Lausanne)</li>
    <li>3D Visualisierungen von Landschaftsveränderungen (Universität Zürich)</li>
</ul> 

<a>Das Projekt wird von der ETH Zürich, der Universität Lausanne, der Eawag und der Universität Zürich in Kooperation durchgeführt und von MeteoSchweiz im Rahmen von GCOS Schweiz (<a href="https://www.meteoschweiz.admin.ch/ueber-uns/forschung-und-zusammenarbeit/programme-gaw-ch-und-gcos-ch.html">mehr Infos</a>) finanziert. </a>

<h1>3D-Visualisierungen von Landschaftsveränderungen</h1>

<a>Am <a href="www.geo.uzh.ch">Geographischen Institut der Universität Zürich</a> wurden Softwarepackete getestet und passende Workflows entwickelt, um die hochaufgelösten Daten als interaktive, webbasierte 3D-Visualisierungen erlebbar zu machen. Zusätzlich wurden 3D-Zeitreihen berechnet und als Videosequenzen bereitgestellt, um trotz technischer Einschränkungen die hohe Detailgenauigkeit der Daten und die dynamischen Landschaftsveränderungen zugänglich zu machen.</a>

<a> Verantwortlich für das Resultat ist die Forschungsgruppe <a href="https://www.geo.uzh.ch/en/units/3g/research/climate-glacier-change-communication.html"> «Climate and Glacier Change in Communication»</a>. Die Arbeiten wurden ausgeführt von:</a>

